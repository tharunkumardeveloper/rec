export function getWorkoutMessage(
  workout: string,
  type: 'repCount' | 'encouragement' | 'posture' | 'energy' | 'idle',
  count?: number
): string {
  const messages = workoutMessages[workout as keyof typeof workoutMessages];
  if (!messages) {
    // Fallback to generic messages
    return type === 'repCount' && count ? `${count} reps! Keep going!` : "You're doing great!";
  }
  
  const messageArray = messages[type];
  const randomMessage = messageArray[Math.floor(Math.random() * messageArray.length)];
  
  if (count !== undefined && randomMessage.includes('{count}')) {
    return randomMessage.replace('{count}', count.toString());
  }
  
  return randomMessage;
}
